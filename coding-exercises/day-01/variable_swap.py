# Day 01 - Variable Swap Exercise
# Task: Swap the values of two variables without directly using string values
# Goal: Practice variable assignments and temporary storage

# Initial values
glass1 = "milk"
glass2 = "juice"

# Swapping logic
temp=glass1
glass1=glass2
glass2=temp

# Output result
print(glass1+" "+glass2)

# Output: juice milk
